package com.utils.activemq;

public class JmsUtilsException extends Exception {

	private static final long serialVersionUID = 1L;

	public JmsUtilsException() {
		super();
	}

	public JmsUtilsException(String message, Throwable cause) {
		super(message, cause);
	}

	public JmsUtilsException(String message) {
		super(message);
	}

	public JmsUtilsException(Throwable cause) {
		super(cause);
	}

}
